package com.example.DispatcherModule;

public class user {
    public String name;
    public String email;
    public String phone;
    public String password;
    public String type;

    public user(){}

    public user(String name, String email, String phone,String password,String type) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.password = password;
        this.type = type;
    }
}
